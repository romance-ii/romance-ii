package org.starhope.appius.util;

/**
 * This is a convenience interface for things that must have a
 * user-visible name. Complements the toString method for
 * programmer-visible names.
 * 
 * @author ewinkelman@resinteractive.com Ed Winkelman
 * @author twheys@gmail.com Timothy W. Heys
 * @author ewinkelman@resinteractive.com Ed Winkelman
 * @author brpocock@star-hope.org Bruce-Robert Pocock
 */
public interface HasName {
	/**
	 * Return a user-visible, unique name for this class. This name may
	 * not necessarily be globally unique, but must be unique across
	 * all objects of this class. For example, having both a User and a
	 * Zone named “Lightning” is acceptable, but there may not be two
	 * users with the same name.
	 * 
	 * @return a user-visible string name for this instance
	 */
	public String getName ();
}
