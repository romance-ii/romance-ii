package org.starhope.vergil.net;

import java.util.List;

import org.json.JSONObject;

/**
 * @author ewinkelman@resinteractive.com Ed Winkelman
 * @author twheys@gmail.com Timothy W. Heys
 * @author ewinkelman@resinteractive.com Ed Winkelman
 * @author brpocock@star-hope.org Bruce-Robert Pocock
 */
public interface AppianJSONTransport {
	/**
	 * Set the URI for the server to be used.
	 * 
	 * @param uri the server URI to be used.
	 */
	public void setServerURI (String uri);
	
	/**
	 * Send a JSON packet to the server and receive back the result
	 * set.
	 * 
	 * @param toSend The JSON data (envelope and all) to be transmitted
	 * @return the list of result packets returned by the server
	 */
	public List <JSONObject> transact (JSONObject toSend);
}
